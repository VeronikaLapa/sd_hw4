package com.demo.sd.app.model;


public class Stock {

    private Integer id;

    private String company;

    private long price;

    private long count;

    public Integer getId() {
        return id;
    }

    public String getCompany() {
        return company;
    }

    public long getPrice() {
        return price;
    }

    public long getCount() {
        return count;
    }
}
